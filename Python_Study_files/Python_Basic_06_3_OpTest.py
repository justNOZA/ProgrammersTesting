#연산자 (대입, 산술)

a = 10 #대입
print(1+2) #더하기(산술)
print(1-2) #빼기(산술)
print(1*2) #곱하기(산술)
print(2**10) #거듭제곱(산술)
print(10/3) #나누기(산술)_실수반환
print(10/2)
print(10//6) #정수나누기(산술) _정수부분만(소수는 버림)
print(10%6) #나머지(산술)

#증감연산자가 따로 없다.
a = a+1
a += 1
#위의 둘중 하나를 써야한다. (복합 대입 연산자)

