#입력할 진법(2/8/10/16) : 16
#입력값 : ff
#출력할 진법(2/8/10/16) : 2
#결과 : ff(16) -> 0b11111111(2)
a = int(input("입력할 진법(2/8/10/16) : "))
b = input("입력값 : ")
c = int(input("변환할 진법 (2/8/10/16)"))
d = int(b,a)
if c == 2 :
    print("결과 : ",b,"(",a,")" "->" ,bin(d),"(",c,")",sep='')
elif c == 8 :
    print("결과 : ",b,"(",a,")" "->" ,oct(d),"(",c,")",sep='')
elif c == 16 :
    print("결과 : ",b,"(",a,")" "->" ,hex(d),"(",c,")",sep='')
else :
    print("결과 : ",b,"(",a,")" "->" ,d,"(",c,")",sep='')
